package com.example.smartcampus.entity;

public class MeetRoom {
    private String meetroom_name;
    private String meetroom_place;
    private int meetroom_peonum;
    private int meetroom_key;

    public String getMeetroom_name() {
        return meetroom_name;
    }

    public void setMeetroom_name(String meetroom_name) {
        this.meetroom_name = meetroom_name;
    }

    public String getMeetroom_place() {
        return meetroom_place;
    }

    public void setMeetroom_place(String meetroom_place) {
        this.meetroom_place = meetroom_place;
    }

    public int getMeetroom_peonum() {
        return meetroom_peonum;
    }

    public void setMeetroom_peonum(int meetroom_peonum) {
        this.meetroom_peonum = meetroom_peonum;
    }

    public int getMeetroom_key() {
        return meetroom_key;
    }

    public void setMeetroom_key(int meetroom_key) {
        this.meetroom_key = meetroom_key;
    }
}
